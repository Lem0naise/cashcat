const colors = ['#84D684', '#bac2ff', '#f2602f', '#7B7BBD', '#246644'];
const sizes = ['text-3xl', 'text-4xl', 'text-5xl', 'text-6xl'];
const opacities = ['opacity-15', 'opacity-20', 'opacity-25'];

const generateRandomAnimationStyle = () => {
    const duration = 10 + Math.random() * 15; 
    const delay = -(Math.random() * duration); 
    const startX = Math.random() * 100;
    const startY = Math.random() * 100;
    const xMovement = (Math.random() - 0.5) * 80; 
    const yMovement = (Math.random() - 0.5) * 80; 
    const rotation = (Math.random() - 0.5) * 14; 
    
    return {
        '--float-duration': `${duration}s`,
        '--float-delay': `${delay}s`,
        '--float-x': `${xMovement}px`,
        '--float-y': `${yMovement}px`,
        '--float-rotation': `${rotation}deg`,
        '--start-x': `${startX}%`,
        '--start-y': `${startY}%`,
    } as React.CSSProperties;
};

const generateRandomIconProps = () => ({
    size: sizes[Math.floor(Math.random() * sizes.length)],
    color: colors[Math.floor(Math.random() * colors.length)],
    opacity: opacities[Math.floor(Math.random() * opacities.length)],
    animationStyle: generateRandomAnimationStyle()
});

const FloatingIconsBackground = () => {
    return (
        <div className="fixed bg-background inset-0 -z-10 pointer-events-none">
            {iconPaths.map((iconContent, index) => {
                const props = generateRandomIconProps();
                
                return (
                    <svg
                        key={index}
                        className={`absolute ${props.size} ${props.opacity} animate-random-float`}
                        style={{
                            ...props.animationStyle,
                            left: 'var(--start-x)',
                            top: 'var(--start-y)',
                            transform: 'translate(-50%, -50%)' 
                        }}
                        viewBox="0 0 24 24"
                        fill={props.color}
                        width="1em"
                        height="1em"
                    >
                        {iconContent}
                    </svg>
                );
            })}
        </div>
    );
};
export default FloatingIconsBackground;